defmodule Servy.Handler do
  @moduledoc """
  Handles http request
  """
  require Logger
  import Servy.Plugins, only: [rewrite_path: 1, emojify: 1, track: 1, log: 1, format_response: 1]
  import Servy.Parser, only: [parse: 1]
  import Servy.HandleFile, only: [read_file: 2]
  alias Servy.{Conv, BearController}

  @pages_path Path.expand("pages", File.cwd!())

  def handle(request) do
    request
    |> parse
    |> rewrite_path
    |> route
    |> log
    |> emojify
    |> track
    |> format_response
  end

  def route(%Conv{method: "GET", path: "/wildthings"} = conv) do
    %Conv{conv | resp_body: "Bears, Lions, Tigers", status: 200}
  end

  def route(%Conv{method: "GET", path: "/bears"} = conv) do
    BearController.index(conv)
  end

  def route(%Conv{method: "GET", path: "/bear/" <> id} = conv) do
    BearController.show(conv, Map.put(conv.params, "id", id))
  end

  def route(%Conv{method: "DELETE", path: "/bear/" <> _id} = conv) do
    BearController.delete(conv)
  end

  def route(%Conv{method: "POST", path: "/bears", params: params} = conv) do
    BearController.create(conv, params)
  end

  def route(%Conv{method: "GET", path: "/pages/" <> file} = conv) do
    @pages_path
    |> Path.join(file <> ".html")
    |> read_file(conv)
  end

  def route(%Conv{method: "GET", path: "/about"} = conv) do
    @pages_path
    |> Path.join("about.html")
    |> read_file(conv)
  end

  def route(%Conv{method: "GET", path: "/bears/new"} = conv) do
    @pages_path
    |> Path.join("form.html")
    |> read_file(conv)
  end

  def route(%Conv{} = conv) do
    %Conv{conv | resp_body: "No path here!", status: 404}
  end
end

